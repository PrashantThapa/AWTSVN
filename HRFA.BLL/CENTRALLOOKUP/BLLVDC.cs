using System;
using System.Collections.Generic;
using HRFA.ATT;
using HRFA.DataLayer;
namespace HRFA.BLL
{
    public class BLLVDC
    {
        public List<ATTDistrictVDC> GetVDC(int? DistrictCD, int? VdcCD)
        {
            try
            {
                DLLVDC lst = new DLLVDC();

                return lst.GetVDC(DistrictCD, VdcCD);
            }
            catch (Exception ex)
            {
                throw ex;
            }
        }
    }
}
