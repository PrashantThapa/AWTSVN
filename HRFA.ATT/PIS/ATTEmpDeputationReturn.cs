using System;

namespace HRFA.ATT
{
    public class ATTEmpDeputationReturn
    {
        public ATTEmpDeputationReturn()
        {
            _Office = new ATTOffice();
        }
        public Int64? SubmissionNo { get; set; }
        public Int64? OldSubmissionNo { get; set; }

        private ATTOffice _Office;
        public ATTOffice Office
        {
            get { return _Office; }
            set { _Office = value; }
        }

        public Int16? EmpID { get; set; }
        public string EmployeeName { get; set; }
        public string DeputationFromDate {get; set;}
        public string ReturnDate { get; set; }
        public string Remarks { get; set; }
        public string FromDate { get; set; }
        public string ToDate { get; set; }
        public string EntryBy { get; set; }
        public string EntryDate { get; set; }
        public string RStatus { get; set; }
        public string Action { get; set; }

    }
}
