using System.Collections.Generic;

namespace HRFA.ATT
{
    public class ATTEmpGradeLoan
    {
        private List<ATTLoan> _Loans ;
        public List<ATTLoan> Loans
        {
            get { return _Loans; }
            set { _Loans = value; }
        }
        private List<ATTEmpGrade> _EmpGrade ;
        public List<ATTEmpGrade> EmpGrade
        {
            get { return _EmpGrade; }
            set { _EmpGrade = value; }
        }
        public ATTEmpGradeLoan()
        {
            _Loans = new List<ATTLoan>();
            _EmpGrade = new List<ATTEmpGrade>();
        }
    }
}
